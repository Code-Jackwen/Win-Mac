public class Lambda {
//    简介：
//    可以取代大部分的匿名内部类，尤其在集合的遍历。
//    对接口的要求：
//    Lambda 规定接口中只能有一个需要被实现的方法，不是规定接口中只能有一个方法
//    jdk 8 中有另一个新特性：default，
//    被 default 修饰的方法会有默认实现，不是必须被实现的方法，所以不影响 Lambda 表达式的使用。

//    @FunctionalInterface#
//    修饰函数式接口，要求接口中的抽象方法只有一个。
//    这个注解往往会和 lambda 表达式一起出现。

//    Lambda 基础语法#
//    我们这里给出六个接口，后文的全部操作都利用这六个接口来进行阐述。

//    主要优点：
//     1.代码变得更加简洁紧凑
//     2.可读性强，
//     3.并行操作大集合变得很方便，可以充分发挥多核cpu的优势，更加便于多核处理器编写代码等，
//    lambda无法单独出现，需要一个函数式接口来盛放，可以说lambda表达式方法体是函数式接口的实现

//    -> :可以理解为“被用于”的意思
}
